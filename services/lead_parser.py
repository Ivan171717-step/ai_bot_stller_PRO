import re


def parse_quick_lead(text: str, username: str | None = None) -> dict:
    t = (text or "").lower()

    business = "Не указано"
    if "сто" in t or "автосервис" in t:
        business = "автосервис / СТО"
    elif "недвиж" in t:
        business = "недвижимость"
    elif "курс" in t or "обучен" in t:
        business = "курсы / обучение"
    elif "часы" in t:
        business = "магазин часов"
    elif "olx" in t:
        business = "OLX"
    elif "маркетплейс" in t:
        business = "маркетплейс"
    elif "webapp" in t or "вебапп" in t:
        business = "WebApp"
    elif "crm" in t or "срм" in t:
        business = "CRM"
    elif "faq" in t or "поддержк" in t:
        business = "поддержка / FAQ"
    elif any(x in t for x in ["кафе", "ресторан", "суш", "доставка еды"]):
        business = "кафе / ресторан / доставка еды"
    elif "металлолом" in t:
        business = "металлолом"
    elif "салон" in t:
        business = "салон красоты"
    elif "магазин" in t:
        business = "магазин одежды" if "одежд" in t else "магазин"
    elif "достав" in t:
        business = "доставка суши" if "суш" in t else "доставка"
    elif any(x in t for x in ["ремонт", "услуг", "строит", "клининг", "юрист", "медицин", "стомат", "фитнес", "спортзал", "грузоперевоз", "такси", "турагент", "отел", "аренд"]):
        business = "услуги"

    bot_by_business = {
        "автосервис / СТО": "бот для автосервиса",
        "недвижимость": "бот для недвижимости / заявок",
        "курсы / обучение": "бот для обучения / курсов",
        "магазин часов": "бот для продажи часов",
        "OLX": "бот уведомлений / обработки OLX",
        "маркетплейс": "бот мониторинга маркетплейсов",
        "WebApp": "Telegram WebApp-бот",
        "CRM": "бот с CRM / базой заявок",
        "поддержка / FAQ": "бот поддержки клиентов",
        "кафе / ресторан / доставка еды": "бот доставки еды",
        "металлолом": "бот для приёма заявок на металлолом",
        "салон красоты": "бот для записи клиентов",
        "магазин": "бот-магазин",
        "магазин одежды": "бот-магазин",
        "доставка": "бот доставки",
        "доставка суши": "бот доставки",
        "услуги": "бот для приёма заявок",
    }

    bot_type = bot_by_business.get(business, "Не указано")
    if "webapp" in t:
        bot_type = "Telegram WebApp-бот"

    func_map = {
        "каталог": ["каталог", "товары", "карточки товаров", "позиции"],
        "заявки": ["заявки", "заявка", "лиды", "принимать клиентов", "сбор контактов"],
        "уведомления админу": ["уведомления админу", "уведомления менеджеру", "присылать менеджеру", "отправлять админу", "сообщение админу"],
        "CRM": ["crm", "срм", "база клиентов", "база заявок", "статусы заявок"],
        "Google Sheets": ["google sheets", "гугл таблицы", "таблица", "таблицы"],
        "оплата": ["оплата", "платежи", "онлайн оплата", "принимать оплату"],
        "корзина": ["корзина", "добавить в корзину", "оформление заказа"],
        "запись": ["запись", "записывать клиентов", "расписание", "свободное время"],
        "геолокация": ["геолокация", "адрес", "местоположение", "карта"],
        "фото": ["фото", "изображение", "картинки", "загрузка фото"],
        "AI-консультант": ["ai консультант", "ии консультант", "искусственный интеллект", "отвечает клиентам", "умные ответы"],
        "AI-память": ["память", "помнить клиента", "история диалога"],
        "рассылка": ["рассылка", "акции", "уведомлять клиентов"],
        "парсинг": ["парсинг", "парсер", "собирать данные с сайтов", "искать по сайтам"],
        "мониторинг цен": ["мониторинг цен", "отслеживание цен", "падение цены", "изменение цены"],
        "WebApp": ["webapp", "вебапп", "мини приложение", "приложение внутри телеграм"],
    }
    funcs = [name for name, keys in func_map.items() if any(k in t for k in keys)]
    functions = ", ".join(funcs) if funcs else (text or "")

    payments = "Да" if any(x in t for x in ["оплат", "платеж"]) else "Нет"
    ai_needed = "Да" if any(x in t for x in [" ai", "ии", "консультант", "искусственный интеллект"]) else "Нет"
    urgency = "Очень срочно" if "сроч" in t else "Обычный срок"

    phone_match = re.search(r"(\+?\d[\d\s\-\(\)]{8,}\d)", text or "")
    nick_match = re.search(r"@\w+", text or "")
    contact = nick_match.group(0) if nick_match else (phone_match.group(1) if phone_match else (f"@{username}" if username else "Не указано"))

    return {
        "business": business,
        "bot_type": bot_type,
        "functions": functions,
        "payments": payments,
        "ai_needed": ai_needed,
        "urgency": urgency,
        "phone": contact,
        "comment": text or "",
    }


def build_quick_lead_preview(data: dict) -> str:
    return (
        "Черновик заявки 📝\n\n"
        f"Бизнес: {data.get('business', 'Не указано')}\n"
        f"Тип бота: {data.get('bot_type', 'Не указано')}\n"
        f"Функции: {data.get('functions', 'Не указано')}\n"
        f"Оплата: {data.get('payments', 'Не указано')}\n"
        f"AI-консультант: {data.get('ai_needed', 'Не указано')}\n"
        f"Срочность: {data.get('urgency', 'Обычный срок')}\n"
        f"Контакт: {data.get('phone', 'Не указано')}\n\n"
        "Начнём с 1000 грн.\n"
        "Финальная стоимость зависит от задач и будет согласована с разработчиком"
    )
