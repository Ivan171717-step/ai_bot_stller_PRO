def analyze_internal_complexity(data: dict | str) -> dict:
    text = data if isinstance(data, str) else " ".join(str(v) for v in data.values())
    t = text.lower()

    medium = ["ai", "google sheets", "crm", "оплат", "каталог", "корзин", "рассыл", "olx", "email", "роли админа"]
    hard = ["webapp", "парс", "мониторинг цен", "маркетплейс", "ebay", "vinted", "prom", "rozetka", "api", "личный кабинет", "аналитика", "память", "прокси", "real-time", "изображ"]

    if any(k in t for k in hard):
        return {
            "complexity": "hard",
            "admin_note": "Есть признаки сложного проекта: парсинг, WebApp, маркетплейсы, API, мониторинг цен или AI-память.",
            "price_comment": "Оценивать отдельно. Возможна длительная разработка и уточнение API/парсинга.",
            "questions": [
                "какие площадки подключать?",
                "есть ли официальный API?",
                "нужна ли авторизация?",
                "как часто обновлять данные?",
                "нужны ли прокси?",
                "какие данные сохранять?",
                "нужен ли WebApp или достаточно обычного Telegram-бота?",
            ],
        }
    if any(k in t for k in medium):
        return {
            "complexity": "medium",
            "admin_note": "Есть признаки средней сложности: каталог, CRM, оплата, AI, таблицы или уведомления.",
            "price_comment": "Оценивать после уточнения функций и интеграций.",
            "questions": [
                "нужна ли админ-панель?",
                "нужна ли Google Таблица или CRM?",
                "нужна ли онлайн-оплата?",
                "нужен ли AI-консультант?",
            ],
        }
    return {
        "complexity": "simple",
        "admin_note": "Похоже на простую заявку: меню, кнопки, форма заявки, уведомления.",
        "price_comment": "Обычно можно оценивать как простой бот после уточнения деталей.",
        "questions": [
            "какие кнопки нужны?",
            "куда отправлять заявки?",
            "нужно ли сохранять клиентов?",
        ],
    }
